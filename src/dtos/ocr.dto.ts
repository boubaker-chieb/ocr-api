import { Line, RecognizeResult, Word } from "tesseract.js";

export interface OcrDto {
  text?: string | null;
  html?: string | null;
  paragraphs?: object[] | null;
  lines?: Line[];
  words?: Word[];
  symbols?: Word[];
}

export const toOcrDTo = (ocrResult: RecognizeResult): OcrDto => {
  return {
    text: ocrResult.data.text,
    html: ocrResult.data.hocr
  };
};

